#include <iostream>
using namespace std;
int main() {

    int number1, number2, sum;

    cout << "Enter the first number: ";
    cin >> number1;

    cout << "Enter the second number: ";
    cin >> number2;

    sum = number1 + number2;

    cout << "The sum of " << number1 << " and " << number2 << " is " << sum << endl;

    return 0;
}

