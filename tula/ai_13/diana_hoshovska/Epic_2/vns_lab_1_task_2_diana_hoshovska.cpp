#include <iostream>
using namespace std;

int main()
{
    int m, n, k, x, y;
    cout << "Enter m: ";
    cin >> m;
    cout << "Enter n: ";
    cin >> n;

    k = n---m;
    x = m--<n;
    y = n++>m;

    cout << "k = ";
    cout << k << endl;

    cout << "x = ";
    cout << x << endl;

    cout << "y = ";
    cout << y << endl;

    return 0;
}