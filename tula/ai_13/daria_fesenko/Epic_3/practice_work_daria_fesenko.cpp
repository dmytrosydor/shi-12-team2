#include <iostream>
#include <vector>
#include <string>

using namespace std;

int main() {
    vector<string> назвиКниг = {"Книга 1", "Книга 2", "Книга 3"};
    vector<bool> доступністьКниг = {true, true, true};

    int вибір;

    do {
        cout << "Головне меню:\n";
        cout << "1. Переглянути всі книги\n";
        cout << "2. Взяти книгу\n";
        cout << "3. Повернути книгу\n";
        cout << "4. Вийти\n";

        cout << "Оберіть операцію (1-4): ";
        cin >> вибір;

        switch (вибір) {
            case 1:
                cout << "У бібліотеці " << назвиКниг.size() << " книг:\n";
                for (const auto &назва : назвиКниг) {
                    cout << назва << endl;
                }
                break;

            case 2:
                cout << "Список усіх книг:\n";
                for (int i = 0; i < назвиКниг.size(); ++i) {
                    cout << i + 1 << ". " << назвиКниг[i] << " (" << (доступністьКниг[i] ? "Доступна" : "Взята") << ")\n";
                }

                cout << "Введіть номер книги, яку хочете взяти: ";
                int індексВзяття;
                cin >> індексВзяття;

                if (індексВзяття >= 1 && індексВзяття <= назвиКниг.size()) {
                    if (доступністьКниг[індексВзяття - 1]) {
                        cout << "Ви взяли книгу \"" << назвиКниг[індексВзяття - 1] << "\".\n";
                        доступністьКниг[індексВзяття - 1] = false;
                    } else {
                        cout << "Книга вже взята.\n";
                    }
                } else {
                    cout << "Некоректний номер книги.\n";
                }
                break;

            case 3:
                cout << "Список усіх книг:\n";
                for (int i = 0; i < назвиКниг.size(); ++i) {
                    cout << i + 1 << ". " << назвиКниг[i] << " (" << (доступністьКниг[i] ? "Доступна" : "Взята") << ")\n";
                }

                cout << "Введіть номер книги, яку хочете повернути: ";
                int індексПовернення;
                cin >> індексПовернення;

                if (індексПовернення >= 1 && індексПовернення <= назвиКниг.size()) {
                    if (!доступністьКниг[індексПовернення - 1]) {
                        cout << "Ви повернули книгу \"" << назвиКниг[індексПовернення - 1] << "\".\n";
                        доступністьКниг[індексПовернення - 1] = true;
                    } else {
                        cout << "Ця книга вже є в бібліотеці.\n";
                    }
                } else {
                    cout << "Некоректний номер книги.\n";
                }
                break;

            case 4:
                cout << "До побачення!\n";
                break;

            default:
                cout << "Некоректний ввід. Будь ласка, оберіть існуючу операцію.\n";
        }

        cout << "Бажаєте виконати інші операції? (1 - Так, 0 - Ні): ";
        cin >> вибір;

    } while (вибір != 0);

}
